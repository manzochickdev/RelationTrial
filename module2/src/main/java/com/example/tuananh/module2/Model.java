package com.example.tuananh.module2;

public class Model {
    int id;
    String name;

    public Model(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Model() {
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
